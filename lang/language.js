let translations = {};

function applyLanguage(lang) {
    translations = translationsData[lang] || translationsData.fi;

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        if (translations[key]) el.textContent = translations[key];
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if (translations[key]) el.placeholder = translations[key];
    });

    document.querySelectorAll("[data-i18n-title]").forEach(el => {
        const key = el.dataset.i18nTitle;
        if (translations[key]) el.title = translations[key];
    });

    localStorage.setItem("language", lang);
    document.documentElement.lang = lang;
    document.dispatchEvent(new Event("languageChanged"));
}

function t(key) {
    return translations[key] || key;
}

function setupCustomSelect() {
    // Update to use button clicks instead of dropdown
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            applyLanguage(lang);
            // Update active state
            document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
        });
    });
}

function loadLanguage() {
    const savedLang = localStorage.getItem("language") || "fi";
    applyLanguage(savedLang);
    setupCustomSelect();
    // Set active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === savedLang) {
            btn.classList.add('active');
        }
    });
}

document.addEventListener("DOMContentLoaded", loadLanguage);

// Make applyLanguage globally available for debugging
window.applyLanguage = applyLanguage;
